const mongoose = require("mongoose");
const Schema = mongoose.Schema;

const tempUserSchema = new Schema({
	firstname: {
		type: String,
		required: true,
	},
	lastname: {
		type: String,
		required: true,
	},
	email: {
		type: String,
		required: true,
	},
	date: {
		type: Date,
		default: Date.now,
	},
});

module.exports = mongoose.model("tempUsers", tempUserSchema);
